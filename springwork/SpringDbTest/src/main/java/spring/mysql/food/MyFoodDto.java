package spring.mysql.food;

public class MyFoodDto {
	
	private String f_num;
	private String h_num;
	private String f_name;
	private String f_photo;
	private int f_price;
	private String f_grade;
	
	public String getF_num() {
		return f_num;
	}
	public void setF_num(String f_num) {
		this.f_num = f_num;
	}
	public String getH_num() {
		return h_num;
	}
	public void setH_num(String h_num) {
		this.h_num = h_num;
	}
	public String getF_name() {
		return f_name;
	}
	public void setF_name(String f_name) {
		this.f_name = f_name;
	}
	public String getF_photo() {
		return f_photo;
	}
	public void setF_photo(String f_photo) {
		this.f_photo = f_photo;
	}
	public int getF_price() {
		return f_price;
	}
	public void setF_price(int f_price) {
		this.f_price = f_price;
	}
	public String getF_grade() {
		return f_grade;
	}
	public void setF_grade(String f_grade) {
		this.f_grade = f_grade;
	}
	
	
	

}
